package org.aibles.failwall.user.dto.request;

import org.aibles.failwall.validation.opt.OtpConstraint;
import javax.validation.constraints.Email;
import javax.validation.constraints.NotBlank;

public class ActiveUserFormRequestDto {

    @NotBlank(message = "email must not be null or blank")
    @Email(message = "email not valid", regexp = "(?:[a-z0-9!#$%&'*+/=?^_`{|}~-]+(?:\\.[a-z0-9!#$%&'*+/=?^_`{|}~-]+)*|\"(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21\\x23-\\x5b\\x5d-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])*\")@(?:(?:[a-z0-9](?:[a-z0-9-]*[a-z0-9])?\\.)+[a-z0-9](?:[a-z0-9-]*[a-z0-9])?|\\[(?:(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.){3}(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?|[a-z0-9-]*[a-z0-9]:(?:[\\x01-\\x08\\x0b\\x0c\\x0e-\\x1f\\x21-\\x5a\\x53-\\x7f]|\\\\[\\x01-\\x09\\x0b\\x0c\\x0e-\\x7f])+)\\])")
    private String email;

    @OtpConstraint
    @NotBlank(message = " otp must not be null or blank")
    private String otp;

    /**
     * form active user
     *
     * @param email String
     * @param otp String
     */
    public ActiveUserFormRequestDto(String email, String otp) {
        this.email = email;
        this.otp = otp;
    }

    public ActiveUserFormRequestDto() {
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOtp() {
        return otp;
    }

    public void setOtp(String otp) {
        this.otp = otp;
    }
}
